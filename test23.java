
public class test23 {
	public static void main (String args[]){
		System.out.println(Math.abs(-26.7)); //absolute number
		System.out.println(Math.ceil(7.4)); //rounding, ceil always rounds it up
		System.out.println(Math.floor(7.8)); //rounding, floor always rounds it down
		System.out.println(Math.max(8.6,5.2));
		System.out.println(Math.min(8.6,5.2));
		System.out.println(Math.pow(5,3));
		System.out.println(Math.sqrt(9));
	}
}
